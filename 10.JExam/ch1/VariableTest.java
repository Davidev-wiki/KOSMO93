package a.b.c;

public class VariableTest {

	public static void main(String[] args) {
		
		    //-----데이터 구분------
		    //데이터 : 문자 & 문자열
		    //숫자 : 정수 & 실수
		    //-------------------- 
			//변수 : 데이터를 담는 공간
			//--------------------
			
			int i = 1;
			
			// int형 데이터 타입으로 변수명을 i로 선언-종결(;).
			// i 변수명에 값 1을 대입(=)한다. *할당 & 초기화
			
			// int형 변수 i를 호출(invoke)한다.
			System.out.println(i);
		 

	} //end of main 함수

} //end of VariableTest 클래스